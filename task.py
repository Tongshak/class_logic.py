# A simple calculator

allowance = 2000

allowance -= 400
print(f"remain: {allowance}")

allowance  += 100
print(f"add: {allowance}")

allowance -=250
print(f"bough snacks: {allowance}")

allowance *=0.75
print(f"gave 25% out: {allowance}")

allowance = ((2/3) * allowance)
print(f"used one-third: {allowance}")

allowance = ((allowance) //2)
print(f"split: {allowance}")

allowance %= 100
print(f"final: {allowance}")


